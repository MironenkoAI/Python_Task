# срезы 
my_list = [2, 4, 6, 2, 8, 10, 12, 14, 16]
print(my_list[2:7:2]) # [6, 8, 12]  start:stop(не включается):step
print(my_list[:7:2])  # [2, 6, 8, 12] не указан старт = 0
print(my_list[2::2])  # [6, 8, 12, 16] не указан стор = до конца
print(my_list[2:7:])  # [6, 2, 8, 10, 12] не указан шаг = 1
print(my_list[8:3:-1])  # [16, 14, 12, 10, 8]  отрицательный шаг = справа налево
print(my_list[3::])     # [2, 8, 10, 12, 14, 16] 
print(my_list[:7:])     # [2, 4, 6, 2, 8, 10, 12]
