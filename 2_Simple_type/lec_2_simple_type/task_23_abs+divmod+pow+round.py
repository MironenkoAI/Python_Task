x = -42
print(abs(x)) # возвращает всегда положительное число

a = 42
b = 5
print(divmod(a, b)) # возвращает кортеж (целое при делении a на b, остаток от деления)

print(pow(a, b)) # возвращает a в степени b
print(pow(a, b, 10)) # возвращает остаток от деления на 10 от a в степени b

print(round(3.141_592_653_589_793, 3)) # округлить до указанного символа